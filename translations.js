let currentLanguage = localStorage.getItem('app_language') || 'en';

function setLanguage(lang) {
      currentLanguage = lang;
      localStorage.setItem('app_language', lang);
      applyTranslations();
      
      // Dispatch event for other scripts to respond
      window.dispatchEvent(new CustomEvent('languageChanged', { detail: lang }));
}

function applyTranslations() {
      const texts = translations[currentLanguage];
      if (!texts) return;
  
      // Find all elements with data-i18n attribute
      const elements = document.querySelectorAll('[data-i18n]');
      elements.forEach(el => {
                const key = el.getAttribute('data-i18n');
                if (texts[key]) {
                              if (el.tagName === 'INPUT' && el.type === 'text') {
                                                el.placeholder = texts[key];
                              } else {
                                                el.textContent = texts[key];
                              }
                }
      });
  
      // Find all elements with data-i18n-title attribute
      const titleElements = document.querySelectorAll('[data-i18n-title]');
      titleElements.forEach(el => {
                const key = el.getAttribute('data-i18n-title');
                if (texts[key]) {
                              el.title = texts[key];
                }
      });
  
      // Update Language Buttons highlighting
      const langBtns = document.querySelectorAll('.lang-btn');
      langBtns.forEach(btn => {
                // We can check the onclick attribute to see which language it sets
                if (btn.getAttribute('onclick').includes(`'${currentLanguage}'`)) {
                              btn.classList.add('active');
                } else {
                              btn.classList.remove('active');
                }
      });
  
      // Update HTML lang attribute
      document.documentElement.lang = currentLanguage;
}

// Helper to get a translation from elsewhere
function getTranslation(key, params = {}) {
      let text = translations[currentLanguage][key] || key;
      for (const [k, v] of Object.entries(params)) {
                text = text.replace(`{${k}}`, v);
      }
      return text;
}

document.addEventListener('DOMContentLoaded', () => {
      applyTranslations();
});
